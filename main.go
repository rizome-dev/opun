import (
	"log"
	"os"
	"os/exec"
)

func main() {
	// Run the actual command from cmd/opun
	cmd := exec.Command("go", append([]string{"run", "./cmd/opun"}, os.Args[1:]...)...)
	cmd.Stdin = os.Stdin
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr

	if err := cmd.Run(); err != nil {
		log.Fatal(err)
	}
}
